<section id="Orthophoniste" class="doctors section light-background">

    <!-- Section Title -->
    <div class="container section-title" data-aos="fade-up">
        <h2>Qu'est-ce que l'orthophonie</h2>
    
    </div><!-- End Section Title -->

    <div class="container">

        <div class="row">

            <div class="Orthophoniste Psychomotricité">
                <div class="d-flex align-items-center justify-content-center ">
                    <img class="rounded-3" src="assets/img/photos/orthophoniste.jpeg" style="width: 50%;max-height: 50vh;">
                </div> <!-- Bannière -->
                    <div class="content text-center text-break px-5">
                    <p>
                        L'orthophonie est une profession paramédicale qui intervient auprès de tout public, des enfants aux adultes, pour prévenir, évaluer et traiter les troubles du langage, de la voix et de la communication. Elle a pour objectif d'améliorer l'autonomie du patient dans ses capacités à s’exprimer, comprendre et interagir avec son environnement.
                    </p>
                    <p>
                        L’orthophoniste utilise des techniques variées, telles que la rééducation du langage, la gestion de la voix, et la prise en charge des troubles de la communication verbale et non verbale, afin de permettre à chaque patient de mieux s'épanouir dans son quotidien.
                    </p>
                    <p>
